/**
 * Get a random motivational quote
 */
export function getRandomQuote(): string {
  const randomIndex = Math.floor(Math.random() * motivationalQuotes.length);
  return motivationalQuotes[randomIndex];
}

/**
 * Get a quote based on the day of the year (ensures different quote each day)
 */
export function getDailyQuote(date: Date = new Date()): string {
  // Calculate day of year (1-365/366)
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  
  // Use day of year to select quote (cycles through all quotes)
  const quoteIndex = dayOfYear % motivationalQuotes.length;
  return motivationalQuotes[quoteIndex];
}

/**
 * Get total number of quotes available
 */
export function getTotalQuotes(): number {
  return motivationalQuotes.length;
}
